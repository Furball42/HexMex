using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Cube
{    
    public float X { get; set; }
    public float Y { get; set; }
    public float Z { get; set; }

    public Cube(float x, float y, float z){
        X = x;
        Y = y;
        Z = z;
    }
}
